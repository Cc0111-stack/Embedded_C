/*
 * ============================================================================
 *  使用 C 语言模拟面向对象的多态（Polymorphism）
 *
 *  核心思路：
 *  1. 通过结构体嵌套（struct usa_person 内嵌 struct person）模拟"继承"
 *  2. 通过函数指针（void (*get_name)(...)）模拟"虚函数表"
 *  3. 基类的函数指针指向一个"跳板函数"（trampoline），该函数将
 *     基类指针强制转换为派生类指针，再调用派生类重写的函数
 * ============================================================================
 */

#include <string.h>
#include <stdio.h>


/* ============================= 基类 ============================= */
struct person {
    char name[12];                                    /* 成员变量：姓名 */
    int age;                                          /* 成员变量：年龄 */
    void (*get_name)(struct person *p, char *name);   /* 成员函数：获取姓名（相当于虚函数） */
};

/* ============================= 派生类 ============================= */
struct usa_person {
    struct person p;                                  /* 内嵌基类对象（非指针），模拟"继承" */
    void (*get_name)(struct usa_person *p, char *name); /* 派生类自己的函数指针（重写） */
};

/* ============================= 函数定义 ============================= */

/*
 * get_name —— 基类的"默认实现"，同时也是 C 模拟多态的"跳板函数"
 *
 * 工作流程：
 *   1. 接收一个 struct person * 指针（实际指向的是 usa_person 对象）
 *   2. 强制转换为 struct usa_person *
 *   3. 调用派生类的 get_name（即 usa_person.get_name）
 *     （相当于 C++ 中通过 vtable 调用派生类重写的虚函数）
 *
 * 之所以需要这个跳板，是因为：
 *   - 基类 person 的函数指针签名固定为 (struct person *, char *)
 *   - 派生类 usa_person 的函数指针签名是 (struct usa_person *, char *)
 *   - 二者参数类型不同，不能直接赋值
 *   - 通过跳板函数做一次类型转换，实现"基类指针调用派生类方法"的效果
 */
static void get_name(struct person *p, char *name) {
    /* 向下转型：将基类指针强制转换为派生类指针 */
    struct usa_person *child = (struct usa_person *)p;

    /* 调用派生类的重写函数（多态分派） */
    child->get_name(child, name);
}

/*
 * get_usa_name —— 派生类 usa_person 对 get_name 的"重写"
 *
 * 在名字后追加 "-usa" 后缀，模拟 C++ 派生类重写基类虚函数的行为
 */
static void get_usa_name(struct usa_person *p, char *name) {
    sprintf(name, "%s", p->p.name);              /* 从基类成员获取姓名 */
    sprintf(name + strlen(name), "%s", "-usa");  /* 追加后缀 */
}

/* ============================= 对象初始化 ============================= */

static struct usa_person usa_p;   /* 全局对象（也可以放在堆上） */

/*
 * init_usa_p —— 初始化 usa_p 对象
 *
 * 设置关键函数指针：
 *   - usa_p.p.get_name = get_name      （基类函数指针指向跳板函数）
 *   - usa_p.get_name   = get_usa_name   （派生类函数指针指向重写后的函数）
 *
 * 当通过基类指针调用 get_name 时：
 *   person->get_name → get_name(跳板) → usa_person->get_name → get_usa_name
 *                     ↑ 模拟 vtable 的间接跳转
 */
static void init_usa_p() {
    strcpy(usa_p.p.name, "cai");
    usa_p.p.get_name = get_name;     /* 基类的虚函数指针 → 跳板函数 */
    usa_p.get_name = get_usa_name;   /* 派生类的重写函数指针 → 实际实现 */
}

/* ============================= 主函数 ============================= */

int main() {
    init_usa_p();

    char name[12] = {0};
    struct person *p = (struct person *)&usa_p;  /* 基类指针指向派生类对象（向上转型） */

    /*
     * 多态调用：
     *   表面调用的是 person.get_name
     *   实际经过跳板函数转发到 usa_person.get_name
     *   最终执行 get_usa_name
     *
     * 输出结果：name is cai-usa
     */
    p->get_name(p, name);
    printf("name is %s\n", name);
    return 0;
}


/*
 * ============================================================================
 *  C 与 C++ 在多态实现上的区别
 * ============================================================================
 *
 * ┌─────────────────────┬──────────────────────────┬─────────────────────────────┐
 * │       方面           │          C 语言           │          C++ 语言           │
 * ├─────────────────────┼──────────────────────────┼─────────────────────────────┤
 * │ 多态支持              │ 语言本身不直接支持，         │ 语言原生支持，通过 virtual     │
 * │                     │ 需手动模拟                 │ 关键字声明虚函数               │
 * ├─────────────────────┼──────────────────────────┼─────────────────────────────┤
 * │ 虚函数表 (vtable)     │ 手动维护函数指针结构体      │ 编译器自动生成 vtable 和        │
 * │                     │ 并手动初始化               │ vptr，初始化由构造函数完成       │
 * ├─────────────────────┼──────────────────────────┼─────────────────────────────┤
 * │ 继承                 │ 通过结构体嵌套模拟，        │ 使用 : public 语法，编译器      │
 * │                     │ 需手动处理内存布局对齐       │ 自动处理基类子对象布局          │
 * ├─────────────────────┼──────────────────────────┼─────────────────────────────┤
 * │ 向上转型              │ 需要显式强制类型转换       │ 隐式转换，自动将派生类指针        │
 * │ (upcasting)         │                           │ 转换为基类指针                │
 * ├─────────────────────┼──────────────────────────┼─────────────────────────────┤
 * │ 向下转型              │ 需要显式强制类型转换       │ 安全向下转型需用                │
 * │ (downcasting)       │ 无运行时类型检查，不安全   │ dynamic_cast（含 RTTI 检查）    │
 * ├─────────────────────┼──────────────────────────┼─────────────────────────────┤
 * │ 多态分派              │ 手动通过跳板函数完成       │ 编译器自动通过 vtable 完成       │
 * │                     │ 继承层级每增加一层需        │ 虚函数调用自动解析到             │
 * │                     │ 额外编写一个跳板函数       │ 正确的重写函数                  │
 * ├─────────────────────┼──────────────────────────┼─────────────────────────────┤
 * │ 类型安全              │ 弱                         │ 强（编译器类型检查 + RTTI）   │
 * ├─────────────────────┼──────────────────────────┼─────────────────────────────┤
 * │ 代码量 / 维护成本      │ 高（需要大量样板代码）     │ 低（语法简洁，编译器代劳）         │
 * ├─────────────────────┼──────────────────────────┼─────────────────────────────┤
 * │ 运行效率              │ 极优（无额外运行时开销）      │ 极优（vtable 间接调用         │
 * │                     │                           │ 仅比 C 多一次指针解引用）     │
 * ├─────────────────────┼──────────────────────────┼─────────────────────────────┤
 * │ 封装 (private/       │ 无语言支持，通过            │ class + public/protected/   │
 * │ public)             │ 头文件暴露/隐藏实现约定      │ private 语法原生支持           │
 * ├─────────────────────┼──────────────────────────┼─────────────────────────────┤
 * │ 构造函数 / 析构函数    │ 需要手动编写 init_* 和     │ 编译器自动调用构造函数和         │
 * │                     │ cleanup_* 系列函数         │ 析构函数（RAII）              │
 * └─────────────────────┴──────────────────────────┴─────────────────────────────┘
 *
 *
 *  总结：
 *    用 C 模拟面向对象多态是完全可行的，内核（Linux kernel）、嵌入式系统、
 *    协议栈等大量 C 项目中广泛使用这种模式（如 file_operations 结构体）。
 *
 *    但这是"手动挡"——编译器不会帮你检查类型一致性、不会自动初始化虚函数表、
 *    不会自动调用构造/析构函数。每一个跳板函数、每一次指针转换都需要开发者
 *    自行确保正确。
 *
 *    C++ 则是"自动挡"——同样的目标，编译器帮你完成了绝大部分工作，代价是
 *    引入了 RTTI、异常等运行时机制，以及在资源受限的嵌入式场景中可能不需要
 *    的额外开销。
 *
 *    选择哪种语言取决于场景：
 *      - 对资源极度敏感（MCU、RTOS）→ C 的手动模拟更可控
 *      - 强调开发效率、复杂对象层级 → C++ 的原生支持更安全高效
 * ============================================================================
 */