#include <string.h>
#include <stdio.h>

/**
 * ============================================================
 *  使用 C 语言模拟面向对象的封装（Encapsulation）
 *
 *  语言背景：
 *  ┌─────────────────────┬────────────────────────────────────┐
 *  │   C 语言            │  C++ 语言                          │
 *  ├─────────────────────┼────────────────────────────────────┤
 *  │  无 class 关键字     │  class / struct 原生支持封装        │
 *  │  无 public/private   │  public / protected / private      │
 *  │  数据 + 方法分离      │  数据 + 方法统一放在类内部          │
 *  │  靠头文件约定访问权限  │  编译器强制访问控制                 │
 *  └─────────────────────┴────────────────────────────────────┘
 *
 *  核心思路：
 *    1. 将数据（成员变量）放在 struct 中
 *    2. 将操作数据的方法以"函数指针"的形式也放在 struct 中
 *    3. 调用时通过 对象.函数指针(...) 或 对象指针->函数指针(...)
 *       达到类似 C++ 中 对象.方法() 的效果
 *
 *  这样数据和方法就被"封装"在了同一个结构体内，形成"对象"的概念。
 * ============================================================
 */

/**
 * struct person —— 模拟面向对象中的"类"
 *
 * ┌──────────────────────────┐
 * │        person            │
 * ├──────────────────────────┤
 * │ 成员变量：               │
 * │   char name[12]         │  ← 数据：姓名
 * │   int age               │  ← 数据：年龄
 * ├──────────────────────────┤
 * │ 成员函数（函数指针）：     │
 * │   get_name()            │  ← 方法：获取姓名
 * │   get_age()             │  ← 方法：获取年龄
 * └──────────────────────────┘
 *
 * 函数指针的声明格式：
 *   void (*get_name)(struct person *p, char *name);
 *         ↑         ↑             ↑              ↑
 *      返回值    函数指针名    参数1：this指针  参数2：输出缓冲区
 *
 * 第一个参数 struct person *p 相当于 C++ 中的 this 指针，
 * 用于在函数内部访问调用该函数的对象本身。
 */
struct person {
    char name[12];                                    /* 成员变量：姓名 */
    int age;                                          /* 成员变量：年龄 */
    void (*get_name)(struct person *p, char *name);   /* 成员函数：获取姓名 */
    void (*get_age)(struct person *p, int *age);      /* 成员函数：获取年龄 */
};

/**
 * get_name —— "成员函数"的实现
 *
 * 相当于 C++ 中的：
 *   class person {
 *   public:
 *       void get_name(char *name) { strcpy(name, this->name); }
 *   };
 *
 * 区别在于 C 需要手动将 this 指针（即 p）作为第一个参数传入。
 * static 关键字限制链接范围，避免与其他文件的同名函数冲突。
 *
 * @param p     指向 person 对象的指针（相当于 this 指针）
 * @param name  输出缓冲区，用于接收姓名
 */
static void get_name(struct person *p, char *name) {
    strcpy(name, p->name);
}

/**
 * get_age —— "成员函数"的实现
 *
 * 相当于 C++ 中的：
 *   class person {
 *   public:
 *       void get_age(int *age) { *age = this->age; }
 *   };
 *
 * @param p    指向 person 对象的指针（相当于 this 指针）
 * @param age  输出参数，通过指针返回年龄值
 */
static void get_age(struct person *p, int *age) {
    *age = p->age;
}

/**
 * 全局对象 p1 —— 模拟"实例化"一个对象
 *
 * 使用 C99 的"指定初始化器"（Designated Initializer）语法：
 *   .成员名 = 值
 *
 * 这里做了两件事：
 *   1. 初始化成员变量：.name / .age
 *   2. 绑定成员函数：.get_name / .get_age
 *      将函数指针指向具体实现函数
 *
 * 这一步相当于 C++ 构造函数所做的工作：
 *   - 初始化数据成员
 *   - 建立 vtable（虚函数表）—— C++ 编译器自动完成
 *   而在 C 中我们需要手动绑定函数指针。
 */
static struct person p1 = {
    .name = "cai",
    .age = 10,
    .get_name = get_name,       /* 绑定函数到函数指针 */
    .get_age = get_age,         /* 绑定函数到函数指针 */
};

int main() {
    char name[12] = {0};        /* 输出缓冲区，全部初始化为 0 */
    int age = 0;                /* 存放年龄的变量 */

    struct person *p = &p1;     /* 指针 p 指向全局对象 p1 */

    /**
     * 通过函数指针调用方法
     *
     * 语法：p->get_name(p, name)
     *       ↑          ↑
     *    访问函数指针  传入 this 指针（即 p 自身）
     *
     * 这模拟了 C++ 中的 p->getName(name)
     * 但 C++ 的 this 指针由编译器隐式传递，无需手动传入。
     */
    p->get_name(p, name);       /* 调用 get_name，结果存入 name 数组 */
    p->get_age(p, &age);        /* 调用 get_age，结果通过 age 指针返回 */

    printf("name is %s, age = %d \n", name, age);

    return 0;
}


/**
 * ============================================================
 *  封装性对比：C vs C++
 * ============================================================
 *
 * ┌──────────────────────┬───────────────────────┬──────────────────────┐
 * │       对比项          │       C 语言          │      C++ 语言        │
 * ├──────────────────────┼───────────────────────┼──────────────────────┤
 * │ 数据 + 方法放在一起   │  struct + 函数指针     │  class 原生支持       │
 * │ this 指针            │ 显式作为参数传入        │ 隐式传递，关键字 this  │
 * │ 访问控制（public等）  │ 无语言支持             │ public/protected/     │
 * │                      │ 靠约定（头文件暴露）     │ private 编译器强制    │
 * │ 构造函数              │ 手动 init_* 函数       │ 编译器自动调用         │
 * │ 方法绑定              │ 手动赋值函数指针        │ 编译器自动建立 vtable  │
 * │ 类型安全              │ 弱（函数指针类型需匹配） │ 强（编译期检查）       │
 * │ 运行开销              │ 极小（仅一次指针调用）   │ 极小（同函数指针调用）  │
 * └──────────────────────┴───────────────────────┴──────────────────────┘
 *
 *  总结：
 *    C 语言的封装靠的是"程序员自律"和"编码约定"，
 *    C++ 的封装靠的是"编译器强制"和"语言语法"。
 *    两者目标相同，但 C 需要更多的"手工活"。
 *
 *    在嵌入式 C 开发中，这种 struct + 函数指针 的模式非常常见，
 *    例如 Linux 内核的 file_operations、驱动框架中的 ops 结构体等。
 * ============================================================
 */