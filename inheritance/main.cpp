#include <stdio.h>

/**
 * ============================================
 *  使用 C 语言模拟面向对象的继承（Inheritance）
 *
 *  语言背景：
 *    C 语言中没有 class、继承等面向对象语法，
 *    但可以通过"结构体嵌套"来实现"继承"的效果。
 *
 *  核心思路：
 *    在一个结构体（派生类）内部嵌入另一个结构体（基类）作为第一个成员，
 *    派生类就"拥有"了基类的所有成员变量和函数指针。
 *
 *    内存布局上，派生类对象的起始地址 = 基类成员的起始地址，
 *    这使得将派生类指针强制转换为基类指针是安全的。
 *
 *  典型应用：
 *    - Linux 内核中的继承体系（如 kobject → device → pci_dev）
 *    - 嵌入式驱动中的设备抽象层
 *    - 协议栈中不同层协议的封装
 * ============================================
 */

/**
 * 【基类】 shape（形状）
 *
 * 相当于面向对象中的"基类/父类"（Base class）。
 *
 * 内存布局：
 *   ┌──────────────────┐
 *   │  int width       │  ← 宽度
 *   ├──────────────────┤
 *   │  int height      │  ← 高度
 *   ├──────────────────┤
 *   │  void (*setWidth)│  ← 函数指针：设置宽度
 *   ├──────────────────┤
 *   │  void (*setHeight)│  ← 函数指针：设置高度
 *   └──────────────────┘
 */
struct shape {
    int width;                          /* 宽度 */
    int height;                         /* 高度 */

    /**
     * 函数指针：设置宽度
     * 第一个参数 struct shape* 相当于 C++ 中的 this 指针
     */
    void (*setWidth)(struct shape* s, int w);

    /**
     * 函数指针：设置高度
     */
    void (*setHeight)(struct shape* s, int h);
};

/**
 * shape_setWidth —— 基类 setWidth 的实现
 *
 * static 关键字限制该函数只在当前编译单元可见，
 * 避免与其他文件的同名符号冲突。
 *
 * 相当于 C++ 中的：
 *   void shape::setWidth(int w) { this->width = w; }
 *
 * @param s  指向 shape 对象的指针（this 指针）
 * @param w  要设置的宽度值
 */
static void shape_setWidth(struct shape* s, int w)
{
    s->width = w;
}

/**
 * shape_setHeight —— 基类 setHeight 的实现
 *
 * @param s  指向 shape 对象的指针（this 指针）
 * @param h  要设置的高度值
 */
static void shape_setHeight(struct shape* s, int h)
{
    s->height = h;
}

/**
 * 【派生类】 rectangle（矩形）
 *
 * 模拟 C++ 中的：
 *   class rectangle : public shape { ... };
 *
 * 关键技巧——结构体嵌套：
 *   在 rectangle 中定义 struct shape base 作为第一个成员。
 *   这意味着 rectangle 对象的内存布局如下：
 *
 *   ┌────────────────────────────────┐
 *   │  struct shape base             │
 *   │  ┌──────────────────────────┐  │
 *   │  │  width                   │  │  ← 继承自 shape
 *   │  │  height                  │  │  ← 继承自 shape
 *   │  │  setWidth (函数指针)      │  │  ← 继承自 shape
 *   │  │  setHeight (函数指针)     │  │  ← 继承自 shape
 *   │  └──────────────────────────┘  │
 *   │  int (*getArea)(...)           │  ← 派生类新增的成员
 *   └────────────────────────────────┘
 *
 *   由于嵌套的 base 是第一个成员，所以：
 *     (struct shape*)&rectangle  等价于  &rectangle.base
 *   即可以将派生类指针安全地转换为基类指针（向上转型）。
 *
 *   这模拟了 C++ 中的"单继承"模型——基类子对象位于派生类对象的开头。
 */
struct rectangle {
    struct shape base;                      /* 内嵌基类对象 → 实现"继承" */
    int (*getArea)(struct shape* s);        /* 派生类新增的方法：计算面积 */
};

/**
 * rect_getArea —— 派生类新增方法 getArea 的实现
 *
 * 计算矩形的面积 = 宽度 × 高度
 *
 * @param s  指向 shape 的指针（传入 &rect.base 调用）
 * @return   面积值（int）
 */
static int rect_getArea(struct shape* s) {
    return (s->width * s->height);
}

int main() {
    /* 创建"派生类"对象 rect */
    struct rectangle rect;

    /**
     * 【手动绑定函数指针——模拟 vtable 初始化】
     *
     * 在 C++ 中，编译器会在构造函数中自动完成以下工作：
     *   1. 在对象中设置 vptr（虚函数表指针）
     *   2. vptr 指向类的 vtable（虚函数表）
     *   3. vtable 中保存了所有虚函数的地址
     *
     * 在 C 中，我们需要手动完成"vtable 初始化"：
     *   1. 将基类的函数指针指向具体实现函数
     *   2. 将派生类新增的函数的指针指向具体实现函数
     *
     * 这就是为什么 C 模拟面向对象需要额外的"构造"步骤。
     */
    /* 将基类的 setWidth 函数指针绑定到具体实现 */
    rect.base.setWidth = shape_setWidth;
    /* 将基类的 setHeight 函数指针绑定到具体实现 */
    rect.base.setHeight = shape_setHeight;
    /* 将派生类的 getArea 函数指针绑定到具体实现 */
    rect.getArea = rect_getArea;

    /**
     * 【调用"继承"来的方法】
     *
     * 语法：rect.base.setWidth(&rect.base, 5)
     *       ↑         ↑               ↑
     *    访问基类   通过基类的       传入基类对象的
     *    子对象     函数指针调用      地址作为 this 指针
     *
     * 相当于 C++ 中的：
     *   rect.setWidth(5);    // 编译器自动转换 this 指针
     *   rect.setHeight(7);
     */
    rect.base.setWidth(&rect.base, 5);      /* 设置宽度为 5 */
    rect.base.setHeight(&rect.base, 7);     /* 设置高度为 7 */

    /**
     * 【调用"派生类"自身的方法】
     *
     * 计算矩形面积并打印结果
     * 输出：Total area : 35
     */
    printf("Total area : %d\n", rect.getArea(&rect.base));

    return 0;
}


/**
 * ============================================================
 *  继承机制对比：C vs C++
 * ============================================================
 *
 * ┌──────────────────────┬──────────────────────┬───────────────────────┐
 * │       对比项          │       C 语言          │      C++ 语言         │
 * ├──────────────────────┼──────────────────────┼───────────────────────┤
 * │ 继承实现方式          │ 结构体嵌套            │ class : public 语法   │
 * ├──────────────────────┼──────────────────────┼───────────────────────┤
 * │ 派生类访问基类成员    │ 通过 base.成员名      │ 直接通过成员名访问     │
 * │                      │ 例如：rect.base.width │ 例如：rect.width      │
 * ├──────────────────────┼──────────────────────┼───────────────────────┤
 * │ 向上转型              │ 显式强制类型转换      │ 隐式转换，自动安全     │
 * │ (upcasting)          │ (struct shape*)&rect │ rectangle* → shape*   │
 * ├──────────────────────┼──────────────────────┼───────────────────────┤
 * │ 虚函数表（vtable）    │ 手动维护函数指针      │ 编译器自动生成         │
 * ├──────────────────────┼──────────────────────┼───────────────────────┤
 * │ 构造函数 / 析构函数   │ 手动编写 init /      │ 编译器自动调用         │
 * │                      │ cleanup 函数         │ 支持 RAII              │
 * ├──────────────────────┼──────────────────────┼───────────────────────┤
 * │ 多继承支持            │ 可模拟（嵌套多个      │ 原生支持（菱形继承     │
 * │                      │ 结构体），但极复杂    │ 需虚继承解决）          │
 * ├──────────────────────┼──────────────────────┼───────────────────────┤
 * │ 内存开销              │ 极小（仅数据+指针）   │ 极小（同 C + vptr）    │
 * ├──────────────────────┼──────────────────────┼───────────────────────┤
 * │ 典型应用              │ Linux 内核驱动框架,   │ 大型应用软件开发,      │
 * │                      │ 嵌入式 HAL 层等      │ GUI 框架等             │
 * └──────────────────────┴──────────────────────┴───────────────────────┘
 *
 * 总结：
 *   C 语言的"继承"是通过结构体嵌套 + 手动函数指针管理实现的。
 *   虽然使用起来不如 C++ 方便，但胜在对内存布局的完全可控，
 *   在嵌入式等资源受限的场景中非常实用。
 *
 *   理解 C 语言中的这种模拟方式，更有助于理解 C++ 编译器
 *   在底层做了哪些工作——本质上 C++ 的继承也是类似的内存布局，
 *   只是由编译器替我们完成了这些"手工活"。
 * ============================================================
 */