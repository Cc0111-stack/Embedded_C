/**
 * ============================================
 *  使用 C 语言的函数指针 + 回调模拟"函数重载"
 *
 *  核心思路：
 *    面向对象语言中的"函数重载"（Function Overloading）
 *    通常指：多个同名函数但参数类型/个数不同，编译器自动选择。
 *
 *    C 语言不支持同名函数，但可以通过"回调函数 + 统一接口"
 *    实现类似的效果——即"接口归一化"。
 *
 *    具体做法：
 *      1. 定义统一的函数签名的回调函数类型
 *      2. 分别实现不同类型的具体操作函数
 *      3. 通过函数指针将具体操作传入通用函数
 *
 *    这样做的好处是：调用者只需关心"做什么"，
 *    而不需要关心"对什么类型的数据做"。
 *
 *  典型应用：
 *    - qsort 排序（对不同类型数组排序）
 *    - 驱动层的操作函数表
 *    - 事件回调机制
 * ============================================
 */

#include <stdio.h>

/**
 * exchange_int —— 交换两个 int 类型变量的值
 *
 * 参数使用 void* 泛型指针，以匹配统一接口。
 *
 * @param a  指向第一个 int 的指针
 * @param b  指向第二个 int 的指针
 */
void exchange_int(void *a, void *b)
{
    int* _a = (int *)a;        /* 将 void* 转换为 int* */
    int* _b = (int *)b;        /* 将 void* 转换为 int* */
    int tmp = 0;

    tmp = *_a;                 /* 保存 a 的值到临时变量 */
    *_a = *_b;                 /* 将 b 的值赋给 a */
    *_b = tmp;                 /* 将临时变量的值赋给 b */
}

/**
 * exchange_double —— 交换两个 double 类型变量的值
 *
 * 与 exchange_int 实现了相同的"交换"逻辑，
 * 但处理的是 double 类型。
 *
 * @param a  指向第一个 double 的指针
 * @param b  指向第二个 double 的指针
 */
void exchange_double(void *a, void *b)
{
    double* _a = (double *)a;  /* 将 void* 转换为 double* */
    double* _b = (double *)b;  /* 将 void* 转换为 double* */
    double tmp = 0;

    tmp = *_a;
    *_a = *_b;
    *_b = tmp;
}

/**
 * func —— 统一的函数指针类型（"接口"）
 *
 * 所有符合此签名的函数都可以通过 swap() 调用。
 * 这就是"接口归一化"——不同类型的操作通过同一个"接口"来调用。
 *
 * 参数：
 *   void* —— 第一个操作对象的指针（泛型）
 *   void* —— 第二个操作对象的指针（泛型）
 *
 * 无返回值。
 */
typedef void (*func)(void*, void*);

/**
 * swap —— 通用交换函数（"重载"的实现者）
 *
 * 不关心具体交换的是什么类型的数据，
 * 只负责调用传入的回调函数来完成实际的交换操作。
 *
 * 这模拟了"多态"的效果：
 * 同一个 swap 函数，传入不同的回调函数就表现出不同的行为。
 *
 * @param exchange  指向具体交换函数的函数指针
 * @param a         指向第一个操作对象的指针
 * @param b         指向第二个操作对象的指针
 */
void swap(func exchange, void *a, void *b)
{
    exchange(a, b);     /* 通过函数指针调用实际的交换函数 */
}

int main()
{
    int a = 10;
    int b = 20;
    swap(exchange_int, &a, &b);         /* 传入 exchange_int 回调 */
    printf("a = %d, b = %d\n", a, b);   /* 输出：a = 20, b = 10 */

    double c = 1.23;
    double d = 2.34;
    swap(exchange_double, &c, &d);      /* 传入 exchange_double 回调 */
    printf("c = %f, d = %f\n", c, d);   /* 输出：c = 2.34, d = 1.23 */

    return 0;
}


/**
 * ============================================================
 *  接口归一化对比：C vs C++
 * ============================================================
 *
 * ┌──────────────────────┬───────────────────────┬──────────────────────────┐
 * │       对比项          │       C 语言           │       C++ 语言            │
 * ├──────────────────────┼───────────────────────┼──────────────────────────┤
 * │ 重载方式              │ 函数指针 + 回调        │ 函数重载（同名不同参）      │
 * ├──────────────────────┼───────────────────────┼──────────────────────────┤
 * │ 调用方式              │ swap(func, &a, &b)    │ swap(a, b)               │
 * │                      │ 需显式传入回调函数      │ 编译器自动匹配函数         │
 * ├──────────────────────┼───────────────────────┼──────────────────────────┤
 * │ 类型安全              │ 弱（void* 无类型检查）  │ 强（编译期类型检查）        │
 * ├──────────────────────┼───────────────────────┼──────────────────────────┤
 * │ 泛型能力              │ 手动 + void* 实现      │ 模板（template）原生支持    │
 * ├──────────────────────┼───────────────────────┼──────────────────────────┤
 * │ 代码复用              │ 需要为每个类型写专属函数 │ 模板 + 重载自动生成代码    │
 * ├──────────────────────┼───────────────────────┼──────────────────────────┤
 * │ 运行效率              │ 极优（函数指针间接调用） │ 极优（内联 + 编译期多态）   │
 * └──────────────────────┴───────────────────────┴──────────────────────────┘
 *
 *  总结：
 *    C 语言通过函数指针 + 回调实现了"行为参数化"，
 *    这是 C 语言中最高频使用的多态/重载模式之一。
 *
 *    在嵌入式开发中，这种模式广泛应用于：
 *      - 定时器回调
 *      - 中断回调
 *      - 驱动层的 ops 结构体
 *      - HAL 层抽象接口
 * ============================================================
 */